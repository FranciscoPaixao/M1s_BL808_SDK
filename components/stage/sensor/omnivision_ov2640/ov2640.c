/* OV2640 sensor info */
#define OV2640_ADDR 0x3C
#define SENSOR_ID_REG_H 0x0A
#define SENSOR_ID_REG_L 0x0B

#define SENSOR_ID_H 0x26
#define SENSOR_ID_L 0x42

int ov2640_probe(void)
{
    uint8_t id_h = 0, id_l = 0;
    SCCB_Read_Reg16(OV2640_ADDR, SENSOR_ID_REG_H, &id_h);
    SCCB_Read_Reg16(OV2640_ADDR, SENSOR_ID_REG_L, &id_l);
    printf("-----------camera id %02x%02x \r\n", id_h, id_l);

    if (id_h == SENSOR_ID_H && (id_l == 0x40||id_l == 0x41 || id_l == 0x42)) {
        printf("ID matched\r\n");
        return 0;
    } else {
        printf("NOT matched\r\n");
        return 0;
    }
}

int ov2640_reset(sensor_t *sensor)
{
    int i;
    for (i = 0; i < sizeof(ov2640_reglist)/sizeof(ov2640_reglist[0]); i++){
       SCCB_Write_Reg16(OV2640_ADDR, ov2640_reglist[i][0], (uint16_t)ov2640_reglist[i][1]);
    }

    return 0;
}

